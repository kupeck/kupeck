function sqr() {
    var sqr = document.getElementById("sqr").value;
    document.getElementById("sqrout").value = sqr * sqr;
}
function cub() {
    var cub = document.getElementById("cub").value;
    document.getElementById("cubout").value = cub * cub * cub;
}
function hcu() {
    var hcu = document.getElementById("hcu").value;
    document.getElementById("hcuout").value = hcu * hcu * hcu * hcu;
}
function hhc() {
    var hhc = document.getElementById("hhc").value;
    document.getElementById("hhcout").value = hhc * hhc * hhc * hhc * hhc;
}